    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        int cont = 0;  
        IFrmCliente men = new IFrmCliente(s);      
        mensalidade = (ArrayList<Mensalidade>) s.createQuery("From Mensalidade").list();
        for(Mensalidade m : mensalidade){
            if(m.getNomeMensalidade().equals(txtNome.getText())){
                JOptionPane.showMessageDialog(this, "Nome já está cadastrado!");
                cont = 1;
            }
        }     
        if(campoVazio()){
            if(cont == 0){            
                m = new Mensalidade(txtNome.getText(),
                                    (float)spnValor.getValue(),
                                    txtTipo.getText());

                try{  
                    s.beginTransaction();
                    s.saveOrUpdate(m);
                    s.getTransaction().commit();
                    men.popularCombo();
                    JOptionPane.showMessageDialog(this, "Cadastro realizado com sucesso!");                    
                    atualizarTabela(m, escolha);
                    limparCampos();
                }catch(Exception ex){
                        JOptionPane.showMessageDialog(this, ex);
                }
            }  
        }
        
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int esc = tblMensalidade.getSelectedRow();
        
        if(esc == -1){
            JOptionPane.showMessageDialog(this, "Escolha um valor da tabela! Depois clique em selecionar!",
                "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
        }else{
            if(campoVazio()){
                mensalidade = (ArrayList<Mensalidade>) s.createQuery("From Mensalidade").list();
                for(Mensalidade m : mensalidade){
                    if(m.getIdMensalidade().equals(Integer.parseInt(tblMensalidade.getValueAt(esc, 0).toString()))){                        
                        m.setNomeMensalidade(txtNome.getText());
                        m.setTipo(txtTipo.getText());
                        m.setValor((float)spnValor.getValue());

                        try{ 
                            s.beginTransaction();
                            s.update(m);
                            s.getTransaction().commit();
                            JOptionPane.showMessageDialog(this, "Edição realizada com sucesso!");
                            atualizarTabela(m, esc);
                            limparCampos();
                        }catch(Exception ex){
                            JOptionPane.showMessageDialog(this, ex);
                        }                                               
                    }
                }
            }
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelecionarActionPerformed
        int esc = tblMensalidade.getSelectedRow();
        if(esc == -1){
            JOptionPane.showMessageDialog(this, "Escolha um valor da tabela!",
                "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
        }else{
            txtNome.setText(tblMensalidade.getValueAt(esc, 1).toString());
            txtTipo.setText(tblMensalidade.getValueAt(esc, 2).toString());
            spnValor.setValue(Float.parseFloat(tblMensalidade.getValueAt(esc, 3).toString()));
        }
    }//GEN-LAST:event_btnSelecionarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        mensalidade = (ArrayList<Mensalidade>) s.createQuery("From Mensalidade").list();
        dtm.setRowCount(0);
        int cont = 0;
        for(Mensalidade m : mensalidade){
            if(m.getNomeMensalidade().equals(txtBuscar.getText())){
                dtm.addRow(m.toRow());
                cont = 1;
            }
        }
        if(cont == 0){
            popularTabela();
            JOptionPane.showMessageDialog(this, "Nome não cadastrado!",
                "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

