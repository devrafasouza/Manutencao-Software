    private void atualizarTabela(Mensalidade m, int codigo){
        if (codigo == -1){
            dtm.addRow(m.toRow());
            
        }
        else{
            dtm.removeRow(codigo); 
            dtm.insertRow(codigo, m.toRow());
        }
    }
    
    public void limparCampos(){
       txtNome.setText("");
       txtTipo.setText("");
       spnValor.setValue(2);
   }
    
    public boolean campoVazio(){
        if(txtNome.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Preencha o campo nome!");
            return false;
        }
        if(txtTipo.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Preencha o campo tipo!");
            return false;
        }
        
        return true;
    }
    
    private void popularTabela(){ 
        mensalidade = (ArrayList<Mensalidade>) s.createQuery("From Mensalidade").list();
        dtm.setRowCount(0);        
        for(Mensalidade me : mensalidade){
            dtm.addRow(me.toRow()); 
        } 
    }
    
